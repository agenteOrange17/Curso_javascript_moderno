// Veamos como ir incrementando un valor de 1 en 1 o en determinada cantidad

// Incluir incremento por 1 y menos 1

let puntaje1 = 5;
puntaje1++; //despues de declarar se incrementa resultadi = 6
let puntaje2 = 5;
puntaje2--; //Decrementa uno resultado = 4

//Otra forma de incrementar
let puntaje3 = 5;
++puntaje3; //desde un inicio se incrementa

let puntaje4 = 5;
--puntaje4;

let puntaje5 = 5;
puntaje5 += 3; //aumenta + 3 resultado 8
let puntaje6 = 5;
puntaje6 -= 3; // resta - 3 resultado 2

console.log(puntaje1);
console.log(puntaje2);
console.log(puntaje3);
console.log(puntaje4);
console.log(puntaje5);
console.log(puntaje6);

